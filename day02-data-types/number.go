package main

import "fmt"

// Number adalah tipe data yang digunakan untuk menyimpan nilai numerik.
// Tipe data ini mencakup integer dan float.

func main() {
// integer
	fmt.Println("Satu = ", 1)
	fmt.Println("dua = ", 2)
	fmt.Println("tiga = ", 3)
	fmt.Println("empat = ", 4)
// float
	fmt.Println("satu koma lima = ", 1.5)
}