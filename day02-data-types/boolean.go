package main
	
import "fmt"

// Boolean adalah tipe data yang hanya memiliki dua nilai, yaitu true dan false. 
// Tipe data ini digunakan untuk menyatakan kondisi logika, seperti benar atau salah, ya atau tidak, dan lain sebagainya.

func main() {
	fmt.Println("Benar", true)
	fmt.Println("Salah", false)
}